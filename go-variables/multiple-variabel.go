package main

import "fmt"


func main() {
    // Mendeklarasikan 2 variabel bertipe int.
    var a, b int
    a, b = 10, 20
    fmt.Println(a, b)
    // Output: 10 20

    // Mendeklarasikan 3 variabel bertipe string
    var x, y, z string = "Alice", "Carl", "Eliot"
    fmt.Println(x, y, z)
    // Output: Alice Carl Eliot


}
